# if-elif-else ladder
a=10
if (a==10):
    print("the condition is equal hence enjoy")
elif(a>30):
    print("the condition is greater than 30")
elif(a>50):
    print("the condition is greater than 50")
elif(a>70):
    print("the condition is greater than 70")

else:
    print("the condition is false")


print("done")